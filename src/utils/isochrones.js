/*
  Loading and placement of the isochrone grid asset (public/isochrones/parsec.json,
  generated by scripts/generate_isochrones.py). The asset stores absolute magnitudes
  per filter on a [Fe/H] x log(age) grid; placing an isochrone on the observed CMD
  is a shift by distance modulus and reddening (HR_DIAGRAM_DESIGN.md section 6).
*/

// Extinction coefficients A_band = R_band * E(B-V) for the Pan-STARRS1 bands the
// asset is generated in (Schlafly & Finkbeiner 2011 Table 6, R_V = 3.1)
export const REDDENING_COEFFICIENTS = { gp: 3.172, rp: 2.271, ip: 1.682, zs: 1.322 }

let gridPromise = null

// fetch the grid once per app session; concurrent callers share the same promise
export function loadIsochroneGrid() {
  if (!gridPromise) {
    gridPromise = fetch('/isochrones/parsec.json').then((response) => {
      if (!response.ok) {
        throw new Error(`isochrone grid fetch failed (${response.status})`)
      }
      return response.json()
    })
    // a failed load (offline, bad deploy) may be retried on the next mount
    gridPromise.catch(() => { gridPromise = null })
  }
  return gridPromise
}

export function nearestNode(nodes, value) {
  return nodes.reduce((best, node) => (Math.abs(node - value) < Math.abs(best - value) ? node : best))
}

// the fitting sliders snap to grid nodes, so an exact-match lookup after snapping
export function findIsochrone(grid, feh, logAge) {
  const snappedFeh = nearestNode(grid.feh_nodes, feh)
  const snappedLogAge = nearestNode(grid.log_age_nodes, logAge)
  return grid.isochrones.find((iso) => iso.feh === snappedFeh && iso.log_age === snappedLogAge) || null
}

/*
  Apparent-CMD polyline of an isochrone for a given distance modulus mu and
  reddening E(B-V):
    x (color)     = (M_blue - M_red) + (R_blue - R_red) * E(B-V)
    y (magnitude) = M_red + mu + R_red * E(B-V)
*/
export function placeIsochrone(isochrone, blueFilter, redFilter, mu, ebv) {
  const blueMags = isochrone.mags[blueFilter]
  const redMags = isochrone.mags[redFilter]
  if (!blueMags || !redMags) return null
  const colorShift = (REDDENING_COEFFICIENTS[blueFilter] - REDDENING_COEFFICIENTS[redFilter]) * ebv
  const magShift = mu + REDDENING_COEFFICIENTS[redFilter] * ebv
  return redMags.map((redMag, index) => ({
    x: (blueMags[index] - redMag) + colorShift,
    y: redMag + magShift
  }))
}

// inverse of mu = 5 * log10(distance / 10 pc)
export function distanceParsecs(mu) {
  return Math.pow(10, (mu + 5) / 5)
}

// starting guess for mu from a Gaia parallax (mas); null when parallax is unusable
export function distanceModulusFromParallax(parallaxMas) {
  if (!Number.isFinite(parallaxMas) || parallaxMas <= 0) return null
  return 5 * Math.log10(1000 / parallaxMas) - 5
}
